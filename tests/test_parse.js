import { parseText } from '../parse.js';

// 金牌示例来自 INTERFACE.md
const goldInput = `标题：娘と朝ジョギングした日
主题：日常出来事の自然な描写

——进化过程——
V1：
今朝は娘とジョギングしました。そして、たくさん話しました。

V2：
今朝は娘と一緒にジョギングをして、そのあと色々なことを話しました。

V3：
今朝は娘と一緒にジョギングをしながら、学校のことや最近感じていることについてゆっくり話しました。

——最终定稿——
今朝は娘と一緒にジョギングをしながら、学校のことや最近感じていることについてゆっくり話す時間を持てました。とても穏やかな朝でした。

——本次核心结构——

1. 〜しながら（动作并行）
2. 〜について（话题展开）
3. 抽象感受表达（穏やかな朝）

——表达升级点——

1. 从简单并列句升级为动作并行结构
2. 从“たくさん話しました”升级为具体话题展开
3. 加入情绪性总结句，提高自然度

——错误记录——
[错误1]
错误名称：动词连接过于简单
典型错误说明：使用「そして」进行简单并列，表达偏初级
正确表达模式：使用「〜しながら」或从属结构增强自然度

[错误2]
错误名称：表达过于抽象
典型错误说明：使用「たくさん話しました」缺少具体内容
正确表达模式：加入具体话题（〜のことや〜について）

——生词——
[单词1]
词汇：穏やか
读音：おだやか
中文解释：平静的、温和的
例句：今日は穏やかな天気ですね。

[单词2]
词汇：感じる
读音：かんじる
中文解释：感受到
例句：最近、時間が早く過ぎると感じます。`;

const out = parseText(goldInput);

console.log('=== 解析结果 ===');
console.log(JSON.stringify(out, null, 2));

// 断言：顶层字段
console.assert(out.标题 === '娘と朝ジョギングした日', '标题');
console.assert(out.主题 === '日常出来事の自然な描写', '主题');
console.assert(out.进化过程.includes('V1：'), '进化过程含 V1');
console.assert(out.最终定稿.includes('穏やかな朝'), '最终定稿');
console.assert(out.本次核心结构.includes('〜しながら'), '核心结构');
console.assert(out.表达升级点.includes('情绪性总结句'), '升级点');

// 断言：数组长度
console.assert(out.错误记录.length === 2, '错误记录长度');
console.assert(out.生词.length === 2, '生词长度');

// 断言：第一个错误对象字段
const e1 = out.错误记录[0];
console.assert(e1['错误名称'] === '动词连接过于简单', '错误名称');
console.assert(e1['典型错误说明'].includes('そして'), '典型错误说明');
console.assert(e1['正确表达模式'].includes('〜しながら'), '正确表达模式');

// 断言：第一个单词对象字段
const w1 = out.生词[0];
console.assert(w1['词汇'] === '穏やか', '词汇');
console.assert(w1['读音'] === 'おだやか', '读音');
console.assert(w1['中文解释'] === '平静的、温和的', '中文解释');
console.assert(w1['例句'] === '今日は穏やかな天気ですね。', '例句');

const simpleInput = `标题：2026-03-13 生活习惯｜跑步时听音乐
主题：生活习惯描述

——我想表达——
我经常一边慢跑一边听音乐。

——进化过程——
V1
僕は走るなから、音楽を聞く。

V2
走りながら音楽を聞く。

V3
ジョギングしながら音楽を聞きます。

V4（定稿）
ジョギングしながらよく音楽を聞きます。

——最终定稿——
ジョギングしながらよく音楽を聞きます。

——本次核心结构——
1. AしながらB
表示「一边做A，一边做B」

例：
歩きながら話す
食べながらテレビを見る
ジョギングしながら音楽を聞く

2. よく
表示「经常、常常」，用于描述习惯。

——表达升级点——
1. 用「ジョギングする」代替「走る」，更符合日常运动语境。
2. 加入副词「よく」，表达频率，使句子更完整自然。
3. 使用「AしながらB」结构表达同时进行的两个动作。

——错误记录——
走るなから ❌
走りながら ✔
原因：
「ながら」需要接在 动词ます形去ます 后面。

——生词——
ジョギング（jogging）
慢跑

よく
经常、常常

——学习总结——
表达「一边做A一边做B」时使用：

动词ます形去ます + ながら

——分享标题——
日语真实表达｜「ながら」结构：一边慢跑一边听音乐`;

